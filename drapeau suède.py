import turtle
wn = turtle.Screen()
wn.bgcolor("black")

pen = turtle.Turtle()
pen.speed(2)
pen.color("white")

def flag():
    pen.penup()
    pen.goto(-180,-120)
    pen.pendown()
    pen.begin_fill()
    pen.goto(180,-120)
    pen.goto(180,120)
    pen.goto(-180,120)
    pen.goto(-180,-120)
    pen.fillcolor("#307DC1")
    pen.end_fill()

    pen.penup()
    pen.goto(-180,-20)
    pen.pendown()
    pen.begin_fill()
    pen.goto(180,-20)
    pen.goto(180,20)
    pen.goto(-180,20)
    pen.goto(-180,-20)
    pen.fillcolor("#FFD900")
    pen.end_fill()

    pen.penup()
    pen.goto(-80,-120)
    pen.pendown()
    pen.begin_fill()
    pen.goto(-40,-120)
    pen.goto(-40,120)
    pen.goto(-80,120)
    pen.goto(-80,-120)
    pen.fillcolor("#FFD900")
    pen.end_fill()
flag()
wn.mainloop()